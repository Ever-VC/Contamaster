    private void jbtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnActualizarActionPerformed
        // TODO add your handling code here:
        // Textos predeterminados para cada JTextField de campos no obligatorios
        String placeholderDireccion = "Ingrese la dirección...";
        String placeholderEmail = "Ingrese el correo electrónico...";
        
        String nombres = jtxtNombres.getText();
        String apellidos = jtxtApellidos.getText();
        String sexo = (String) jcmbSexo.getSelectedItem();
        Date fechaNacimiento = jDateChooser1.getDate();
        String direccion = jtxtDireccion.getText().equals(placeholderDireccion) ? "" : jtxtDireccion.getText(); // Si el campo contiene el texto del placeholder (significa que no desea asignar el atreibuto), deja vacia la cadena
        String email = jtxtEmail.getText().equals(placeholderEmail) ? "" : jtxtEmail.getText(); // Si el campo contiene el texto del placeholder (significa que no desea asignar el atreibuto), deja vacia la cadena
        String username = jtxtUsername.getText();
        String password = jtxtPassword.getText();
        // Valida que todos los campos obligatorios tengan datos
        if (!ValidarCamposObligatorios(nombres, apellidos, sexo, username)) {
            return;
        }
        
        // Valida que sea mayor de 18 años
        if (fechaNacimiento != null) {
            if (!validar.ValidarSiEsMayorDeEdad(fechaNacimiento)) {
                JOptionPane.showMessageDialog(null, "LA FECHA DE NACIMIENTO QUE ESTA INGRESANDO NO ES VALIDA, EL SISTEMA NO PERMITE REGISTRO DE USUARIOS MENOSRES DE 18 AÑOS.","ERROR:", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        // Validación de email con expresión regular
        if (!"".equals(email)) { // Valida si el campo no está vacío (significa que sí se desea agregar un email)
            if (!validar.ValidarEmail(email)) { // Valida que el email contenga el formato
                JOptionPane.showMessageDialog(null, "EL [EMAIL] QUE HA INGRESADO NO ES VALIDO, ASEGURESE DE HABERLO ESCRITO CORRECTAMENTE.","ERROR:", JOptionPane.ERROR_MESSAGE);
                return; // Sino coincide, entonces retorna
            }
        }
        
        Usuario usuarioActualizado = UsuarioControlador.Instancia().GetUsuarioPorId(UsuarioCache.Id);// Crea e instancia el nuevo objeto y le asigna los valores del que esta en la sesion actual
        // Insertando la información del nuevo usuario
        usuarioActualizado.setNombres(nombres);
        usuarioActualizado.setApellidos(apellidos);
        usuarioActualizado.setSexo(sexo);
        usuarioActualizado.setFechaNacimiento(fechaNacimiento);
        usuarioActualizado.setDireccion(direccion);
        usuarioActualizado.setEmail(email);
        usuarioActualizado.setUsername(username);
        if (password.equals("Ingrese la nueva contraseña...")) {
            usuarioActualizado.setPassword(null);
        } else {
            usuarioActualizado.setPassword(password);
        }
        
        UsuarioControlador.Instancia().ActualizarUsuario(usuarioActualizado);// Manda a crear el usuario actualizado al controlador
        
        // Actualizar cache de usuario
        UsuarioCache.Nombres = usuarioActualizado.getNombres();
        UsuarioCache.Apellidos = usuarioActualizado.getApellidos();
        UsuarioCache.Sexo = usuarioActualizado.getSexo();
        UsuarioCache.FechaNacimiento = usuarioActualizado.getFechaNacimiento();
        UsuarioCache.Direccion = usuarioActualizado.getDireccion();
        UsuarioCache.Email = usuarioActualizado.getEmail();
        UsuarioCache.Username = usuarioActualizado.getUsername();
        
        // Actualizar la informacion del usuario que se muestra
        Principal frmPrincipal = new Principal();
        frmPrincipal.MostrarInformacionDeUsuario(UsuarioCache.GetNombreApellidoUsuario(), UsuarioCache.Sexo);
        
        JOptionPane.showMessageDialog(null, "EL USUARIO HA SIDO ACTUALIZADO EN LA BASE DE DATOS EXITOSAMENTE, LOS CAMBIOS\nSERAN VISIBLES EL PROXIMO INICIO DE SESION.","TAREA REALIZADA CON EXITO:", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jbtnActualizarActionPerformed
    
